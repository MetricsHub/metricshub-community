import java.io.IOException;
import java.nio.file.Files;
import java.nio.file.Paths;
import java.util.List;

/**
 * This class provides utility methods for reading file contents.
 */
public class FileTool {

	/**
	 * Reads all lines from a file specified by the given file path.
	 *
	 * @param filePath The path to the file to be read.
	 * @return A list of strings, each representing a line in the file.
	 * @throws IOException If an I/O error occurs while reading the file.
	 */
	public List<String> readAllLines(final String filePath) throws IOException {
		return Files.readAllLines(Paths.get(filePath));
	}
}
